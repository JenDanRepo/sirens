<!-- <h3>About SFSiren.net</h3> -->

<p>
	SFSiren.net is the brainchild and hobby of <a href="mailto:danlennon@gmail.com">Dan Lennon</a> (KK6VNY) of Kensington, CA. After he heard that a friend was trying to audit all of the sirens for SF ACS's Siren Net he started wondering which sirens were most freqently checked on and how to determine which sirens nobody had paid attention to in a while.
</p>
<p> 
	Naturally this led to the idea to stuff all the checkins he heard into a database and to start keeping track of which sirens were and weren't being checked. This lead to all kinds of questions about the people checking in on sirens (who does it the most?, or have people adopted one siren they check every week e.g. the one outside of your office.
